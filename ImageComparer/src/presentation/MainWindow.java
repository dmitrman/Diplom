    // загрузить меню из XML 
    private XmlMenuLoader loadMenuBarFromXML() {
         XmlMenuLoader loader = null;
        try {           
          InputStream stream =getClass().getResourceAsStream("/resources/menu.xml");            
                loader = new XmlMenuLoader(stream);
                loader.parse();
                setJMenuBar(loader.getMenuBar("mainMenu"));   
        } catch (IOException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SAXException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        return loader;
    }
   
